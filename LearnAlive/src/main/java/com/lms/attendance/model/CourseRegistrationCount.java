package com.lms.attendance.model;

public class CourseRegistrationCount {
    private int classId;
    private int count;

    // getter & setter
    public int getClassId() {
        return classId;
    }

    public void setClassId(int classId) {
        this.classId = classId;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
